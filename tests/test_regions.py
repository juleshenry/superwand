import numpy as np
from PIL import Image
from superwand.core.np_region_identifier import np_get_prominent_regions

def test_get_prominent_regions(tmp_path):
    img = np.zeros((100, 100, 3), dtype=np.uint8)
    img[:50, :50] = [255, 0, 0]
    img[50:, 50:] = [0, 255, 0]
    img[:50, 50:] = [0, 0, 255]
    img[50:, :50] = [255, 255, 255]

    img_path = tmp_path / "test_regions.png"
    Image.fromarray(img).save(img_path)

    regions = np_get_prominent_regions(str(img_path), number=4)
    assert len(regions) == 4
    for color, indices in regions.items():
        assert len(indices) > 0
