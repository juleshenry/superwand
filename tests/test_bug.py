import numpy as np
from PIL import Image
import os
from superwand.core.np_region_identifier import np_id_regiones


def test_id_regiones(tmp_path):
    # Create a simple 10x10 image with a red square in the middle
    img = np.zeros((10, 10, 3), dtype=np.uint8)
    img[2:5, 2:5] = [255, 0, 0]  # Red square

    img_path = tmp_path / "test_img.png"
    Image.fromarray(img).save(img_path)

    target_color = [255, 0, 0]
    matches = np_id_regiones(str(img_path), target_color, tolerance=20)
    assert len(matches) == 9  # 3x3 square

    # Try with a color that is slightly off
    target_color_off = [250, 5, 5]
    matches_off = np_id_regiones(str(img_path), target_color_off, tolerance=20)
    assert len(matches_off) == 9

    # Try with a color that would cause negative overflow if uint8
    target_color_dark = [10, 10, 10]
    matches_dark = np_id_regiones(str(img_path), target_color_dark, tolerance=20)
    assert len(matches_dark) == (10 * 10 - 9)  # All black pixels
